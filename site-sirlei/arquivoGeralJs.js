function mostrarEsconderTerapias() {

	var exibir = document.getElementById("terapias").style.display;

	if (exibir == "block") {
		document.getElementById("terapias").style.display = "none";
	} else {
		document.getElementById("terapias").style.display = "block";
	}

}

